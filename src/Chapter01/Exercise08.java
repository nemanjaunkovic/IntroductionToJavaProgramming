package Chapter01;

// Exercise08.java
// Write a program that displays the area and perimeter of a
// circle that has a radius of 5.5 using the following formula:
// perimeter = 2 * radius * p a
// area = radius * radius * p

public class Exercise08 {
    public static void main(String[] args) {
        System.out.println("Perimeter = " + (2.0 * 5.5 * 3.14159));
        System.out.println("Area = " + (5.5 * 5.5 * 3.14159));
    }
}